    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            try{
            a.setNumAirplanes(Integer.parseInt(aAir.getText()));
            a.setAirlinerCode(Integer.parseInt(aCode.getText()));
            a.setHomeGround(aGround.getText());
            a.setAirlinerName(aName.getText());
            JOptionPane.showMessageDialog(null, "Airline updated!");
        }
        catch(NumberFormatException n){
            JOptionPane.showMessageDialog(null, "Please enter apt values!");
            aAir.setText("");
            aCode.setText("");
            aGround.setText("");
            aName.setText("");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

