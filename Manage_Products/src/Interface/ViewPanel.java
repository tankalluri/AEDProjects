    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
            txtAvailablity.setEditable(false);
            txtPrice.setEditable(false);
            txtProdName.setEditable(false);
            txtDesc.setEditable(false);
            btnSave.setEnabled(false);
            btnUpdate.setEnabled(true);
            if(!txtDesc.getText().isEmpty() || txtProdName.getText().isEmpty()){
            try{
            product.setAvailNum(Integer.parseInt(txtAvailablity.getText()));
            product.setPrice(Double.parseDouble(txtPrice.getText()));
            product.setDescription(txtDesc.getText());
            product.setName(txtProdName.getText());
            JOptionPane.showMessageDialog(null, "Product updated successfully");
            }
            catch(NumberFormatException e){
            JOptionPane.showMessageDialog(null, "Please enter correct details");
            }
            }
            else{
            JOptionPane.showMessageDialog(null,"Please enter the details");
            }
                     
    }//GEN-LAST:event_btnSaveActionPerformed

    private void backBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backBtnActionPerformed
        panel.remove(this);
        CardLayout layout = (CardLayout)panel.getLayout();
        layout.previous(panel);
        Component c[]= panel.getComponents();
        for(Component comp : c){
            if(comp instanceof ManageProdPanel){
                ManageProdPanel mpp = (ManageProdPanel)comp;
                mpp.populate();
            }
        }
        
    }//GEN-LAST:event_backBtnActionPerformed


