    private void backJButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_backJButtonActionPerformed
        rightPanel.remove(this);
        Component[] componentArray = rightPanel.getComponents();
        Component component = componentArray[componentArray.length - 1];
        LabWorkAreaJPanel dwjp = (LabWorkAreaJPanel) component;
        if(account.getRole() instanceof LabRole)
        dwjp.populateTable();
        else if(account.getRole() instanceof LabAssistantRole)
         dwjp.populateTableEnterprise();
        CardLayout layout = (CardLayout) rightPanel.getLayout();
        layout.previous(rightPanel);
    }//GEN-LAST:event_backJButtonActionPerformed

